<h2>About Arden2ByteCode</h2>

<p>Arden2ByteCode is a compiler that compiles 
<a href="http://en.wikipedia.org/wiki/Arden_syntax">Arden Syntax</a>
to Java Bytecode. It also serves as runtime 
environment for execution of the compiled bytecode.</p>

<p>Arden2ByteCode was developed in collaboration between the 
<a href="http://www.plri.de/index.php?id=Arden2ByteCode">Peter L. Reichertz Institute</a>
for medical computer science and the 
<a href="http://www.ips.cs.tu-bs.de/">Institute for Programming and Reactive Systems</a>. 
Most programming was done by <a href="https://github.com/dgrunwald">Daniel Grunwald</a>.</p>

